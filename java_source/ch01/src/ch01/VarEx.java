package ch01;

public class VarEx {

	public static void main(String[] args) {
		// 타입
		// 정수(4) : char(1byte), int, long, 실수(부동소수점 : 8) float, double, 
		// 100 정수를 출력한다.
		
		char c = 'A';
		System.out.println(c);
		
		System.out.println( (int) c);
		
		// 실수 : 0.01
		double d = 0.01;
		float f = 0.01f;
//		int i = 0.01; // 답은 0
		
		
		System.out.println(d);
		System.out.println(f);
		
		
		int num1 = 10;
		System.out.println( (double)num1  );
		 
		
		
		
		// 문자열
	}

}
