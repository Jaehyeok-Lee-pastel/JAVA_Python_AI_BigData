package ch03;

public class AssignOp {

	public static void main(String[] args) {
		// 복합 대입 연산(줄여쓰기)
		
		int num = 10;
		
		// 변수 num에 5를 더한 후 num 변수에 재입력 후 출력하시오.
		num += 5;
		System.out.println(num);
		
		// 변수 num에 3을 뺀 후 num 변수에 재입력 후 출력하시오.

		num -= 3;
		System.out.println(num);
	}
}
